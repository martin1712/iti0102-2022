

def add(x: int, y: int):
    x + y
    return x + y

def sub(x: int, y: int):
    x - y
    return x - y

def multiply(x: int, y: int):
    x * y
    return x * y

def div(x: int, y: int):
    x / y
    return x / y

def modulus(x: int, y: int):
    y % x
    return y % x

def floor_div(x: int, y: int):
    x // y
    return x // y

def exponent(x: int, y: int):
    x ** y
    return x ** y

def first_greater_or_equal(x: int, y: int):
    x >= y
    return x >= y
